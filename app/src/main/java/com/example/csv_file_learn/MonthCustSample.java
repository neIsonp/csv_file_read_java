package com.example.csv_file_learn;

public class MonthCustSample {

    private String month;
    private int priceForMe, priceForMyGirl;


    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public int getPriceForMe() {
        return priceForMe;
    }

    public void setPriceForMe(int priceForMe) {
        this.priceForMe = priceForMe;
    }

    public int getPriceForMyGirl() {
        return priceForMyGirl;
    }

    public void setPriceForMyGirl(int priceForMyGirl) {
        this.priceForMyGirl = priceForMyGirl;
    }
}
